function onClickButton1() {
	var ansver = document.getElementById('ansver1');
	var buttonText = document.getElementById('button1');
	if (buttonText.innerHTML == "Показать ответ: ") {
		ansver.innerHTML = "2 часа";
		buttonText.innerHTML = "Спрятать ответ";
		}
	else {
		 ansver.innerHTML = "";
		 buttonText.innerHTML = "Показать ответ: ";
	}
}

